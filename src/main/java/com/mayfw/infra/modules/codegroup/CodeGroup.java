package com.mayfw.infra.modules.codegroup;

import java.util.Date;

public class CodeGroup {
	private String seq;
	private String codeGroup;
	private String codeGroupEn;
	private String useNy;
	private Date reg_date;
	private Date mod_date;
	private Integer cnt;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getCodeGroup() {
		return codeGroup;
	}
	public void setCodeGroup(String codeGroup) {
		this.codeGroup = codeGroup;
	}
	public String getCodeGroupEn() {
		return codeGroupEn;
	}
	public void setCodeGroupEn(String codeGroupEn) {
		this.codeGroupEn = codeGroupEn;
	}
	public String getUseNy() {
		return useNy;
	}
	public void setUseNy(String useNy) {
		this.useNy = useNy;
	}
	public Date getReg_date() {
		return reg_date;
	}
	public void setReg_date(Date reg_date) {
		this.reg_date = reg_date;
	}
	public Date getMod_date() {
		return mod_date;
	}
	public void setMod_date(Date mod_date) {
		this.mod_date = mod_date;
	}
	public Integer getCnt() {
		return cnt;
	}
	public void setCnt(Integer cnt) {
		this.cnt = cnt;
	}
	
	
	
	
	

}
